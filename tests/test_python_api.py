@pytest.mark.fast
def test_workflow_run_uses_basepath_for_docker_extract(
    monkeypatch: pytest.MonkeyPatch,
    tmp_path: Path,
) -> None:
    example_path = REPO_ROOT / "examples" / "scripts" / "cwl_builder_workflow.py"
    module = import_python_file(example_path.stem, example_path.resolve())
    workflow = module.build_workflow("hello from test")
    calls: dict[str, list[tuple[Any, ...]]] = {"verify": [], "extract": []}

    def fake_verify(container_engine: str, ignore_install: bool) -> None:
        calls["verify"].append((container_engine, ignore_install))

    def fake_extract(container_engine: str, pull_dir: str, cwl_path: Path) -> None:
        calls["extract"].append((container_engine, pull_dir, cwl_path))

    def fake_run_local(
        run_args_dict: dict[str, str],
        use_subprocess: bool,
        passthrough_args: list[str],
        workflow_name: str,
        basepath: str,
        user_env_vars: dict[str, str] | None = None,
    ) -> int:
        del run_args_dict, use_subprocess, passthrough_args, workflow_name, basepath, user_env_vars
        return 0

    monkeypatch.setattr(python_runtime.pc, "verify_container_engine_config", fake_verify)
    monkeypatch.setattr(python_runtime.pc, "cwl_docker_extract", fake_extract)
    monkeypatch.setattr(python_runtime.rl, "run_local", fake_run_local)

    workflow.run(basepath=str(tmp_path))

    assert calls["verify"] == [("docker", False)]
    assert calls["extract"] == [("docker", str(Path.cwd()), tmp_path / "cwl_builder_workflow_demo.cwl")]


@pytest.mark.fast
def test_compile_python_workflows() -> None:
    """Import and compile all auto-discovered Python workflow scripts."""
    from sophios.apis.python import api  # pylint: disable=C0415:import-outside-toplevel

    global_config = _load_global_config()
    tools_cwl = sophios.plugins.get_tools_cwl(global_config)
    paths_tuples = _iter_python_workflow_paths(global_config)
    import_errors: list[str] = []
    generated_workflows: list[Path] = []
    for path_stem, path in paths_tuples:
        try:
            with _step_registry_injected(tools_cwl):
                module = import_python_file(path_stem, path)
                retval: api.Workflow = module.workflow()

            retval.compile()
            retval.write_ast_to_disk(path.parent)
            generated_workflows.extend(path.parent / f"{wf.process_name}.wic" for wf in retval.flatten_subworkflows())

            config_ci = path.parent / "config_ci.json"
            json_contents = {}
            if config_ci.exists():
                with open(config_ci, mode="r", encoding="utf-8") as r:
                    json_contents = json.load(r)
            run_blacklist: list[str] = json_contents.get("run_blacklist", [])
            subworkflows: list[api.Workflow] = retval.flatten_subworkflows()[1:]
            run_blacklist += [wf.process_name for wf in subworkflows]
            json_contents["run_blacklist"] = run_blacklist
            with open(config_ci, mode="w", encoding="utf-8") as f:
                json.dump(json_contents, f)

        except Exception as exc:  # pylint: disable=W0718:broad-exception-caught
            import_errors.append(f"{path_stem}: {type(exc).__name__}: {exc}")
            traceback.print_exception(type(exc), value=exc, tb=None)
    if import_errors:
        pytest.fail("Python workflow imports failed:\n" + "\n".join(import_errors))
    _write_manifest(generated_workflows)


@pytest.mark.fast
def test_validate_generated_python_workflows() -> None:
    if not PYTHON_WORKFLOW_MANIFEST.exists():
        pytest.fail(f"Missing generated workflow manifest: {PYTHON_WORKFLOW_MANIFEST}")

    global_config = _load_global_config()
    tools_cwl = sophios.plugins.get_tools_cwl(global_config)
    yml_paths = sophios.plugins.get_yml_paths(global_config)
    yaml_stems = utils.flatten([list(paths) for paths in yml_paths.values()])
    validator = wic_schema.get_validator(tools_cwl, yaml_stems, {}, write_to_disk=False)

    workflow_paths = json.loads(PYTHON_WORKFLOW_MANIFEST.read_text(encoding="utf-8"))
    validation_errors: list[str] = []
    for workflow_path_str in workflow_paths:
        workflow_path = Path(workflow_path_str)
        try:
            with workflow_path.open("r", encoding="utf-8") as handle:
                yaml_tree = yaml.load(handle.read(), Loader=wic_loader())
            validator.validate(utils_cwl.desugar_into_canonical_normal_form(yaml_tree))
        except Exception as exc:  # pylint: disable=W0718:broad-exception-caught
            validation_errors.append(f"{workflow_path}: {type(exc).__name__}: {exc}")

    if validation_errors:
        pytest.fail("Generated workflow validation failed:\n" + "\n".join(validation_errors))
