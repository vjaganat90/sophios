REPO_ROOT = Path(__file__).resolve().parent.parent
ADAPTERS = REPO_ROOT / "cwl_adapters"
AUTOGENERATED_DIR = REPO_ROOT / "autogenerated"
PYTHON_WORKFLOW_MANIFEST = AUTOGENERATED_DIR / "python_workflow_manifest.json"


def _adapter(name: str) -> Path:
    return ADAPTERS / f"{name}.cwl"


def _load_global_config() -> Json:
    config_file = Path().home() / "wic" / "global_config.json"
    return io.read_config_from_disk(config_file)


def _iter_python_workflow_paths(global_config: Json) -> list[tuple[str, Path]]:
    paths = sophios.plugins.get_py_paths(global_config)
    return [
        (path_str, path)
        for _, paths_dict in paths.items()
        for path_str, path in paths_dict.items()
        if "mm-workflows" not in str(path) and "docs/tutorials/" not in str(path)
    ]


@contextmanager
def _step_registry_injected(tool_registry: Tools) -> Iterator[None]:
    """Inject a default tool registry into imported Python workflow scripts.

    Args:
        tool_registry (Tools): Registry of known CWL tools used for path fallback.

    Yields:
        Iterator[None]: Context where imported scripts see the patched ``Step``.
    """
    from sophios.apis.python import api  # pylint: disable=C0415:import-outside-toplevel

    step_class = api.Step

    def step_factory(*args: Any, **kwargs: Any) -> Any:
        kwargs.setdefault("tool_registry", tool_registry)
        return step_class(*args, **kwargs)

    with patch.object(api, "Step", step_factory):
        yield


def _write_manifest(workflow_paths: list[Path]) -> None:
    AUTOGENERATED_DIR.mkdir(parents=True, exist_ok=True)
    manifest = sorted({str(path) for path in workflow_paths})
    PYTHON_WORKFLOW_MANIFEST.write_text(json.dumps(manifest, indent=2), encoding="utf-8")


@pytest.mark.fast
def test_explicit_step_ports_match_legacy_yaml() -> None:
    touch_legacy = Step(_adapter("touch"))
    touch_legacy.filename = "empty.txt"
    append_legacy = Step(_adapter("append"))
    append_legacy.file = touch_legacy.file
    append_legacy.str = "Hello"

    touch_explicit = Step(_adapter("touch"))
    touch_explicit.inputs.filename = "empty.txt"
    append_explicit = Step(_adapter("append"))
    append_explicit.inputs.file = touch_explicit.outputs.file
    append_explicit.inputs.str = "Hello"

    legacy_yaml = Workflow([touch_legacy, append_legacy], "wf").yaml
    explicit_yaml = Workflow([touch_explicit, append_explicit], "wf").yaml

    assert legacy_yaml == explicit_yaml


@pytest.mark.fast
def test_in_memory_cwl_step_compiles_through_workflow_api() -> None:
    tool = (
        CommandLineTool(
            "echo_tool",
            Inputs(message=Input(cwl.string, position=1)),
            Outputs(out=Output.stdout()),
        )
        .base_command("echo")
        .stdout("stdout.txt")
    )
    step = Step.from_cwl(tool.to_dict(), process_name="say_hello")
    step.inputs.message = "hello"

    compiled = Workflow([step], "wf").get_cwl_workflow()

    assert compiled["class"] == "Workflow"
    assert compiled["steps"][0]["id"].endswith("say_hello")
    assert compiled["steps"][0]["run"]["class"] == "CommandLineTool"
    assert compiled["steps"][0]["run"]["baseCommand"] == "echo"


