@pytest.mark.fast
def test_config_yaml_normalizes_cwl_file_and_directory_objects(tmp_path: Path) -> None:
    input_dir = tmp_path / "input-dir"
    input_dir.mkdir()
    input_file = tmp_path / "input.txt"
    input_file.write_text("hello", encoding="utf-8")

    subdirectory_cfg = tmp_path / "subdirectory.yml"
    subdirectory_cfg.write_text(
        yaml.safe_dump(
            {
                "directory": {"class": "Directory", "location": str(input_dir)},
                "glob_pattern": ".",
            },
            sort_keys=False,
        ),
        encoding="utf-8",
    )
    subdirectory = Step(_adapter("subdirectory"), config_path=subdirectory_cfg)
    assert subdirectory._yml["in"]["directory"] == {"wic_inline_input": str(input_dir)}

    append_cfg = tmp_path / "append.yml"
    append_cfg.write_text(
        yaml.safe_dump(
            {
                "file": {"class": "File", "location": str(input_file)},
                "str": "Hello",
            },
            sort_keys=False,
        ),
        encoding="utf-8",
    )
    append = Step(_adapter("append"), config_path=append_cfg)
    assert append._yml["in"]["file"] == {"wic_inline_input": str(input_file)}


